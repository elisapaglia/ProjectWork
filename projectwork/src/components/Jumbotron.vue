  <script>

 export default {
    name: 'Jumbotron',
    data() {
            return { //Lista dati:
            words: ["Developer", "Professional Coder", "Designer"],// Lista parole
            currentWordIndex: 0, // Indice nella lista `words`
            currentLetterIndex: 0,// Segnaposto lettera di una parola 
            animatedWord: '', //funzione che costriusce lettera per lettera la funzione
            animationSpeed: 150, // Velocità dell'animazione in millisecondi
            };
        },    

        mounted() {
    this.animateWords();//chiama metodo animateWords
  },
  methods: { //Methods, lista funzioni:
    animateWords() { 

      const currentWord = this.words[this.currentWordIndex];//PAROLA CORRENTE
      if (this.currentLetterIndex < currentWord.length) {//Finchè lettera<lunghezza parola
        this.animatedWord += currentWord[this.currentLetterIndex];// Aggiunge la lettera corrente alla paola che si compone con animatedWord`
        this.currentLetterIndex++; //passa alla prossima lettera della parola che stiamo animando
        setTimeout(this.animateWords, this.animationSpeed); //crea un ritardo, altrimenti sarebbe impercettibile
      } else { //Tutte le lettere della parola sono state esegiute,quindicerchiamo un altra parola,
        setTimeout(() => {
          this.currentWordIndex = (this.currentWordIndex + 1) % this.words.length;
          //Come diventa 0? NON è una divisione ma considera il RESTO/ quindi se non è quamente divisibile quanto avanza?
          //this.currentWordIndex parte da 0
          //Nel 1°ciclo diventa 1, così:(0 + 1) % 3 = 1 % 3 = 1
          //2°ciclo: (1 + 1) % 3 = 2 % 3 = 2
          //3°ciclo: (2 + 1) % 3 = 3 % 3 = 0 //torna a zero e rinizia il ciclo


          this.animatedWord = ''; //azzera la parola da animare, che nel ciclo if verrà arricchita
          this.currentLetterIndex = 0;//azzera lettere
          this.animateWords(); //avvia animazione
        }, 2000); // Ha trovato la parola, attendi 2 sec. || setTimeout(() => { ... }, 2000);: ||
      }
    },
  },
    }//chiusura export

  </script>
  
